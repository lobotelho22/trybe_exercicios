/* O operador NOT inverte o valor de um operador. O sinal != pode ser lido como "não igual" ou "diferente"

Visualizemos as situações abaixo: */

console.log ((2 + 2) === 4); // Nessa situação o console retornará true, que é o valor do teste lógico

console.log (!(2+2) === 4); // Nessa situação o sinal ! exerce a função NOT e inverte o valor do teste lógico. Logo, o resultado será false.

console.log(!0); // curiosidade, o operador NOT pode ser aplicado em números, strings, null, undefined, etc. O zero como é considerado false, negado, retorna um valor true

